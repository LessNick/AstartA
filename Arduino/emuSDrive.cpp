////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Emulate SDrive - незаконченная заготовка для поддержки команд SDrive
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
sDrive::sDrive() {
  m_SDParam.param1 = 0x06;          // Без понятия чё это значит :)
  m_SDParam.param2 = 0x00;          // Продолжаем прикидываться шлангом, то есть SDrive
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
char* sDrive::getIdent() {
  return "SDrive01";
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
SDParam* sDrive::getSDParam() {
  return &m_SDParam;
}

